namespace MelonChart;

/// <summary>
/// This defines the rank status.
/// </summary>
public enum RankStatus
{
    /// <summary>
    /// Identifies the rank undefined.
    /// </summary>
    Undefined,

    /// <summary>
    /// Identifies the rank stayed.
    /// </summary>
    None,

    /// <summary>
    /// Identifies the rank went up.
    /// </summary>
    Up,

    /// <summary>
    /// Identifies the rank went down.
    /// </summary>
    Down,

    /// <summary>
    /// Identifies the rank is new.
    /// </summary>
    New,
}